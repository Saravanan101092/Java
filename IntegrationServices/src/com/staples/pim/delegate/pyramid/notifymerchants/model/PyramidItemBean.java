package com.staples.pim.delegate.pyramid.notifymerchants.model;

public class PyramidItemBean {
	private String	ItemID;
	private String	BrandName;
	private String	ManufacturerPartNum;
	private String	FullDescription;
	private String	ClassName;
	private String	StaplesDotComPM;
	public String getItemID() {
		return ItemID;
	}
	public void setItemID(String itemID) {
		ItemID = itemID;
	}
	public String getBrandName() {
		return BrandName;
	}
	public void setBrandName(String brandName) {
		BrandName = brandName;
	}
	public String getManufacturerPartNum() {
		return ManufacturerPartNum;
	}
	public void setManufacturerPartNum(String manufacturerPartNum) {
		ManufacturerPartNum = manufacturerPartNum;
	}
	public String getFullDescription() {
		return FullDescription;
	}
	public void setFullDescription(String fullDescription) {
		FullDescription = fullDescription;
	}
	public String getClassName() {
		return ClassName;
	}
	public void setClassName(String className) {
		ClassName = className;
	}
	public String getStaplesDotComPM() {
		return StaplesDotComPM;
	}
	public void setStaplesDotComPM(String staplesDotComPM) {
		StaplesDotComPM = staplesDotComPM;
	}
	
}
